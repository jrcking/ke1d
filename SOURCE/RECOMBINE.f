	SUBROUTINE RECOMBINE

	INCLUDE "commonblock"
C	---------------------------------------------------------------
C	AUTHOR: J. R. C. KING
C	---------------------
C	CHANGE RECORD:
C		11-02-2014:	CREATED
C	---------------------------------------------------------------
C	RECOMBINES m0(.,.,.) ACCORDING TO LEVEL SET VALUE...
C	---------------------------------------------------------------
	DO i=1,q
		rho(i) = m0(1,1,i)
		u(i) = m0(1,2,i)/m0(1,1,i)
		E(i) = m0(1,3,i)
		P(i) = m0(1,4,i)
		gamm(i) = gamma0
		Pc(i) = Pc0
	END DO
	DO i=q+1,nr
		rho(i) = m0(2,1,i)
		u(i) = m0(2,2,i)/m0(2,1,i)
		E(i) = m0(2,3,i)
		P(i) = m0(2,4,i)
		gamm(i) = gamma1
		Pc(i) = Pc1
	END DO
C	---------------------------------------------------------------
C	---------------------------------------------------------------
	RETURN
	END
